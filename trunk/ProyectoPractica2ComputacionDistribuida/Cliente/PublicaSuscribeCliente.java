                        Alerta alerta= new Alerta(Objetocliente, interfaz.jRadioButton2.getText().toLowerCase(),interfaz.jList1.getSelectedValue().toString().toLowerCase(),Float.parseFloat(interfaz.jTextField1.getText()));
